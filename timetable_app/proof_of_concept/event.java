public class event {
    String name;
    String dtstart;
    String dtend;
    String uid;
    String location;
    public event(String name, String dtstart, String dtend, String uid, String location) {
        this.name = name;
        this.dtstart = dtstart;
        this.dtend = dtend;
        this.uid = uid;
        this.location = location;
    }
}